    @Override
    public Object cargarDatosPrincipales() {
        String[] columnas = {
            "ID",
            "Valor",
            "Descripción",
            "Estudiante",};
        String[][] datos = this.principal.obtenerTodasLasDeudas(columnas.length);

        DefaultTableModel modelo = new DefaultTableModel(datos, columnas);
        this.jTableDeudas.setModel(modelo);
        return modelo;
    }


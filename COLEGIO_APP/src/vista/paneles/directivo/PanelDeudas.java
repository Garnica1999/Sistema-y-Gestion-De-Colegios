    @Override
    public boolean buscar() {
        String placa = this.jTextFieldValor.getText();
        return this.principal.buscarRuta(placa);
    }

    @Override
    public boolean validar() {
        try{
            if(this.jComboBoxEstudiante.getSelectedIndex() == 0){
                return false;
            } else if(this.jTextPaneDescripcion.getText().equals("")){
                return false;
            } else if(this.jTextFieldValor.getText().equals("")){
                return false;
            }
            
            int codigo = Integer.parseInt(((String)this.jComboBoxEstudiante.getSelectedItem()).split("-")[0]);
            double valor = Double.parseDouble(this.jTextFieldValor.getText());
            String descripcion = this.jTextPaneDescripcion.getText();
            return this.principal.crearDeudaAEstudiante(codigo, valor, descripcion);
            
        }catch(NullPointerException | IndexOutOfBoundsException | NumberFormatException ex){
            System.out.println(ex.getMessage());
            return false;
        }
    }

    private void cargarDatos() {
        cargarDatosPrincipales();
        cargarEstudiantes();
    }
    
    private void cargarEstudiantes(){
        String[] estudiantes = this.principal.obtenerEstudiantesPorCodigo();
        DefaultComboBoxModel<String> modelo = new DefaultComboBoxModel<>();
        modelo.addElement("Estudiantes");
        for(int i = 0 ; i < estudiantes.length; i++){
            modelo.addElement(estudiantes[i]);
        }
        
        this.jComboBoxEstudiante.setModel(modelo);
        
    }

    private void eventuarComponentes() {
        iniciarItemsPopMenu(jTableDeudas, jPopupMenuDeudas);
        eventuarTabs(jTabbedPaneRutas);
    }

    @Override
    public void agregarItemsAMenu() {
        this.jMenuItemAgregarEstudiantes = new JMenuItem("Agregar Estudiantes");
        this.jMenuItemEliminar = new JMenuItem("Eliminar Ruta");
        
        this.jMenuItemAgregarEstudiantes.addActionListener((ActionEvent e) -> {
            agregarEstudiantesARuta();
        });
        
        this.jPopupMenuDeudas.add(jMenuItemAgregarEstudiantes);
        this.jPopupMenuDeudas.add(jMenuItemEliminar);
    }

    private void agregarEstudiantesARuta() {
        String placa = obtenerInformaciónCelda(jTableDeudas, 0).toString();
        this.principal.irAPanelRutasEstudiante(placa);
    }

